# operaciones.py

def suma(a, b):
    resultado = a + b
    return print("El resultado es:", resultado)

def resta(a, b):
    resultado = a - b
    return print("El resultado es:", resultado)

def producto(a, b):
    resultado = a * b
    return print("El resultado es:", resultado)

def division(a, b):
    resultado = a / b
    return print("El resultado es:", resultado)
